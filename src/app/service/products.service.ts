import { Injectable } from '@angular/core';
import { AngularFirestore } from '@angular/fire/compat/firestore';
import { map, Observable } from 'rxjs';
import { of } from 'rxjs';
import { Property } from '../models/property';

@Injectable({
  providedIn: 'root'
})
export class ProductsService {

  constructor(private firestore: AngularFirestore) { }

  // Fetch all properties
  getProperties(): Observable<Property[]> {
    // return of(fakeProperties);
    return this.firestore.collection<Property>('products').valueChanges();
  }

  // // Fetch all properties and filter client-side where inStock does not exist
  // getInStockProperties(): Observable<Property[]> {
  //   return this.firestore.collection<Property>('products').valueChanges().pipe(
  //     map(properties => properties.filter(property => !('inStock' in property)))
  //   );
  // }

  // Fetch a specific property by ID
  getPropertyById(id: string): Observable<Property | undefined> {
    return this.firestore.doc<Property>(`products/${id}`).valueChanges();
  }

  // Log 3 random properties
  logRandomProperties(): void {
    this.getProperties().pipe(
      map(properties => this.shuffleArray(properties).slice(0, 3))
    ).subscribe(randomProperties => {
      console.log('3 Random products:', randomProperties);
    });
  }

  // Utility function to shuffle an array
  private shuffleArray<T>(array: T[]): T[] {
    return array.sort(() => Math.random() - 0.5);
  }
}


